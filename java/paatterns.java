import java.util.*;
public class paatterns{
    public static void main(String[] args)
    {
        int i,j,n;
        Scanner sc= new Scanner(System.in);
        System.out.println("Enter length of pattern");
        n=sc.nextInt();
        for(i=0;i<=n;i++){
            for(j=0;j<=n;j++){
            System.out.print("*");
           
        } 
        System.out.print("\n");
    }
}
}
